  let uid = 1;

  window.createCardInstance = function(def, owner){
    return {
      ...def,
      id:`${owner}_${def.key}_${uid++}`,
      owner,
      maxHp:def.hp || 0,
      hp:def.hp || 0,
      atk:def.atk || 0,
      atkBonus:0,
      movedThisPhase:false,
      counterBonus:0,
      pierceBonus:0,
      canAttack:false,
      summonedTurn:0,
      buffs:[],
      phaseBuffs:[],
      poison:0,
      shieldCharges:def.shield || 0,
      rebirthUsed:false,
      placedAt:0
    };
  };

  function shuffle(list){
    for(let i=list.length-1;i>0;i--){
      const j=Math.floor(Math.random()*(i+1));
      [list[i],list[j]]=[list[j],list[i]];
    }
    return list;
  }

  function takeByCost(pool, plan){
    const selected=[];
    const remaining=[...pool];

    for(const [cost,count] of Object.entries(plan)){
      const matching=shuffle(
        remaining.filter(card=>card.cost===Number(cost))
      );

      for(const card of matching.slice(0,count)){
        selected.push(card);
        const index=remaining.findIndex(item=>item.key===card.key);
        if(index>=0) remaining.splice(index,1);
      }
    }

    return {selected,remaining};
  }

  window.createDeck = function(owner){
    const creaturePool=window.CARD_DEFS.filter(card=>card.category==='creature');
    const spellPool=window.CARD_DEFS.filter(card=>card.category==='spell');

    const creaturePlan={
      1:4,
      2:5,
      3:4,
      4:3,
      5:2,
      6:1,
      7:1
    };

    const creatureResult=takeByCost(creaturePool,creaturePlan);
    const chosenCreatures=creatureResult.selected;

    while(chosenCreatures.length<20 && creatureResult.remaining.length){
      chosenCreatures.push(creatureResult.remaining.shift());
    }

    const chosenSpells=shuffle([...spellPool]).slice(0,10);

    const deck=[
      ...chosenCreatures.map(def=>window.createCardInstance(def,owner)),
      ...chosenSpells.map(def=>window.createCardInstance(def,owner))
    ];

    return shuffle(deck);
  };
